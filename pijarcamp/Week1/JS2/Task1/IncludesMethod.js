// Method includes
/*
Digunakan untuk element tertentu dari sebuah array.
Jika index ada -> true,
Jika tidak ada -> false.
*/
// Syntax
// array.includes('index');
// Contoh penggunaan
let contohIncludes = ['Pramudia', 'Marcheilla', 'Dy', 'Sherlyn'];
console.log(contohIncludes);
// True
console.log(contohIncludes.includes('Pramudia'));
// False
console.log(contohIncludes.includes('Elsa'));