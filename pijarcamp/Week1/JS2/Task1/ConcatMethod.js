// Method concat
/*
Digunakan untuk menggabungkan array (dua atau lebih).
*/
// Syntax
// array1.concat(array2, array3, ..., arrayn);
// Contoh penggunaan
let contohConcat1 = ['Pramudia', 'Marcheilla'];
let contohConcat2 = ['Dy'];
let contohCOncat3 = ['Sherlyn'];

console.log(contohConcat1);
console.log(contohConcat2);
console.log(contohCOncat3);

let contohConcat4 = contohConcat1.concat(contohConcat2, contohCOncat3);
console.log(contohConcat4);
