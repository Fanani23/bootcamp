// Method slice
/*
Digunakan untuk memilih satu atau lebih index dari sebuah array.
*/
// Syntax
// array.slice(start, end);
// Contoh penggunaan
let contohSlice = ['Pramudia', 'Marcheilla', 'Dy', 'Sherlyn'];
console.log(contohSlice);
console.log(contohSlice.slice(0, 2));
console.log(contohSlice.slice(0, 3));