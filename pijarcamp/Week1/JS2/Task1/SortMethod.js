// Method sort
/*
Digunakan untuk mengurutkan index array,
secara ascending maupun descending.
*/
// Syntax
// array.sort();
// Contoh penggunaan
let contohSort = ['Pramudia', 'Marcheilla', 'Dy', 'Sherlyn', 'Elsa'];
console.log(contohSort);
// ascending
contohSort.sort();
console.log(contohSort);
// descending
contohSort.reverse();
console.log(contohSort);